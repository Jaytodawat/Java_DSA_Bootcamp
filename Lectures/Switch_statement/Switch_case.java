package Lectures.Switch_statement;

public class Switch_case {
    public static void main(String[] args) {
        //Switch Statements

        /*
        //Syntax of switch Statement
        switch(expression) {
            //cases
            case 1:
                //do something
                break;
            case 2:
                //do something
                break;
            default:
                //do something
        }
        */

        /*
        //Note:
        - cases must be same type as expression, must be a constant or literal.
        - duplicate case values are not allowed.
        - break is used to terminate the sequence.
        - if break is not used, it will continue to next case.
        - default is used when none of the above does.
        - if default is not at the end, put break after it.
         */
    }
}
